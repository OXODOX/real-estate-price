def extract_dong(address: str) -> str | None:
    """주소에서 읍/면/동 이름을 추출합니다.

    Args:
        address: 주소 (예: "서울 강남구 역삼동 679-13 래미안")

    Returns:
        동 이름 (예: "역삼동") 또는 None
    """
    addr = normalize_address(address)
    tokens = addr.split()

    # 우선순위: 동/읍/면 → 리 (더 세분화된 것 우선)
    found_myeon = None
    for token in tokens:
        if token.endswith(("시", "군", "구", "도")):
            continue
        if token.endswith(("동", "읍")):
            return token
        if token.endswith("면"):
            found_myeon = token
        if token.endswith("리") and len(token) >= 2:
            return token  # 리가 있으면 리 반환

    return found_myeon  # 면만 있는 경우


# 지번 패턴: "숫자" 또는 "숫자-숫자", "산 45", "산45-2" 등
_JIBUN_RE = re.compile(r"^(산\s*)?\d+(-\d+)?$")
# 면적 표기 패턴 (지번으로 오인 방지)
_AREA_SUFFIXES = ("㎡", "평", "m2", "m²")


def extract_jibun(address: str) -> str | None:
    """주소에서 지번을 추출합니다.

    지원 패턴:
    - "679-13", "123" (일반 지번)
    - "산 45", "산 45-2" (산지)

    Args:
        address: 주소 (예: "역삼동 679-13")

    Returns:
        지번 문자열 (예: "679-13") 또는 None
    """
    addr = address.strip()

    # "산 45" 또는 "산45" 처리
    m = re.search(r"산\s*(\d+(?:-\d+)?)", addr)
    if m:
        return "산" + m.group(1)

    # "숫자-숫자" 또는 단독 "숫자"
    tokens = addr.split()
    for token in tokens:
        # 면적 표기인지 확인
        if any(token.endswith(s) for s in _AREA_SUFFIXES):
            continue
        if _JIBUN_RE.match(token):
            return token

    return None


# 단지명으로 쓰이지 않을 법한 접미사 (행정구역/도로명 등)
_NON_BUILDING_SUFFIXES = (
    "시", "군", "구", "도", "동", "읍", "면", "리",
    "로", "길", "번길", "대로",
)

# 단지명이 될 수 없는 시도명/광역시 (단독 토큰)
_SIDO_TOKENS = {
    "서울", "부산", "대구", "인천", "광주", "대전", "울산", "세종",
    "경기", "강원", "충북", "충남", "전북", "전남", "경북", "경남", "제주",
}


def extract_building_name(address: str) -> str | None:
    """주소에서 단지명/건물명 후보를 추출합니다.

    전략: 토큰 중 행정구역·도로명·숫자가 아닌 것을 단지명 후보로 선택 (주로 마지막).

    예:
    - "서울 강남구 역삼동 679-13 래미안그레이튼" → "래미안그레이튼"
    - "서울 강남구 역삼동 래미안그레이튼 101동" → "래미안그레이튼"

    Args:
        address: 주소

    Returns:
        단지명 후보 또는 None
    """
    addr = normalize_address(address)
    tokens = addr.split()

    candidates = []
    for token in tokens:
        # 시도명 단독 토큰 제외 (예: "서울", "경기")
        if token in _SIDO_TOKENS:
            continue
        # 행정구역/도로명 접미사로 끝나면 제외
        if token.endswith(_NON_BUILDING_SUFFIXES):
            continue
        # 지번 패턴 제외
        if _JIBUN_RE.match(token):
            continue
        # 면적/층수 제외
        if any(token.endswith(s) for s in _AREA_SUFFIXES):
            continue
        if token.endswith(("층", "호")):
            continue
        # 분구 있는 시 이름 단독 제외 (예: "용인", "수원")
        if token in _CITIES_WITH_SUBDISTRICTS:
            continue
        # 이제 이 토큰은 단지명 후보
        candidates.append(token)

    if not candidates:
        return None

    # 최소 2글자 이상 (한 글자는 단지명으로 보기 어려움)
    candidates = [c for c in candidates if len(c) >= 2]
    if not candidates:
        return None

    # 여러 후보가 있으면 가장 긴 것을 선택 (보통 단지명이 김)
    return max(candidates, key=len)
