def normalize_address(address: str) -> str:
    """주소를 정규화합니다.

    변환 내용:
    - 시도명 약어 (서울특별시 → 서울, 경기도 → 경기)
    - 분구가 있는 시의 '시' 제거 (용인시 수지구 → 용인 수지구)
    - 불필요한 공백·기호 정리
    - 연속 공백 제거

    Args:
        address: 사용자 입력 주소

    Returns:
        정규화된 주소
    """
    addr = address.strip()

    # 1. 시도명 약어 변환 (긴 이름부터)
    for full, short in _SIDO_ALIASES:
        if addr.startswith(full):
            addr = short + addr[len(full):]
            break

    # 2. 분구 있는 시의 '시' 제거
    # "용인시 수지구" → "용인 수지구"
    for city in _CITIES_WITH_SUBDISTRICTS:
        # 공백 있는 경우: "용인시 수지구"
        addr = re.sub(rf'{city}시\s+([가-힣]+구)', rf'{city} \1', addr)
        # 공백 없는 경우: "용인시수지구"
        addr = re.sub(rf'{city}시([가-힣]+구)', rf'{city} \1', addr)

    # 3. 공백 정리
    addr = " ".join(addr.split())

    return addr


def find_lawd_code(address: str) -> str | None:
    """주소에서 시/군/구의 법정동코드(앞 5자리)를 찾습니다.

    매칭 전략 (우선순위 순):
    1. 정규화 후 전체 맵에서 가장 긴 키부터 매칭
    2. 서울 구 단축 입력 (강남구 → 서울 강남구)
    3. 토큰 기반 조합 매칭

    Args:
        address: 사용자 입력 주소

    Returns:
        법정동코드 5자리 또는 None
    """
    addr = normalize_address(address)

    # 1. 가장 긴 키부터 매칭 (더 구체적인 매칭 우선)
    sorted_keys = sorted(LAWD_CODE_MAP.keys(), key=len, reverse=True)
    for key in sorted_keys:
        if key in addr:
            return LAWD_CODE_MAP[key]

    # 2. 서울 구 단축 입력
    tokens = addr.split()
    for token in tokens:
        if token in _SEOUL_DISTRICTS:
            return LAWD_CODE_MAP[f"서울 {token}"]

    # 3. 토큰 조합 매칭 (2~3개 토큰 조합)
    for n in range(min(3, len(tokens)), 1, -1):
        for i in range(len(tokens) - n + 1):
            candidate = " ".join(tokens[i : i + n])
            if candidate in LAWD_CODE_MAP:
                return LAWD_CODE_MAP[candidate]

    # 4. 마지막 수단: 구/시/군 이름 단독 매칭
    # "○○구"로 끝나는 토큰을 찾아 맵에서 검색
    for token in tokens:
        if token.endswith(("구", "시", "군")):
            for key, code in LAWD_CODE_MAP.items():
                if key.endswith(" " + token) or key == token:
                    return code

    return None


